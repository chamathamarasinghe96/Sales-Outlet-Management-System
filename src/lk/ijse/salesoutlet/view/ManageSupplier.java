    private void btnAddNewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddNewActionPerformed
        String id = lblSupplierID.getText();
        
        String name = txtSupplierName.getText();
        
        if (!Validator.isValidName(name)) {
            JOptionPane.showMessageDialog(this, "Please enter a vaild name");
            txtSupplierName.selectAll();
            txtSupplierName.requestFocusInWindow();
            return;
        }
        
        String address = txtAddress.getText();
        
        if (!Validator.isValidAddress(address)) {
            JOptionPane.showMessageDialog(this, "Please enter a vaild address");
            txtAddress.selectAll();
            txtAddress.requestFocusInWindow();
            return;
        }
        
        String telNo = txtTelephoneNo.getText();
        
        if (!Validator.isValidTelNo(telNo)) {
            JOptionPane.showMessageDialog(this, "Please enter a vaild Telephone Number");
            txtTelephoneNo.selectAll();
            txtTelephoneNo.requestFocusInWindow();
            return;
        }
        
        SupplierDTO supplierDTO = new SupplierDTO(id, name, address, telNo);
        try {
            boolean isAdded = ManageSupplierController.addSupplier(supplierDTO);
            if (isAdded) {
                JOptionPane.showMessageDialog(this, "Supplier Added Successfully");
                clearAllTextFields();
                getAllSupplierDetails();
            } else {
                JOptionPane.showMessageDialog(this, "Failed to Add Supplier due to Some Reasons");
            }
        } catch (Exception ex) {
            Logger.getLogger(ManageSupplier.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnAddNewActionPerformed

