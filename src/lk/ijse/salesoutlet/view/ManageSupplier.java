    private void btnRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveActionPerformed
        int rsp = JOptionPane.showConfirmDialog(this, "Do you want to delete this supplier ? ");
        if (rsp == 0) {
            SupplierDTO supplier = new SupplierDTO(lblSupplierID.getText(), null, null, null);
            try {
                boolean isDeleted = ManageSupplierController.deleteSupplier(supplier);
                if (isDeleted) {
                    clearAllTextFields();
                    getAllSupplierDetails();
                    JOptionPane.showMessageDialog(this, "Supplier Removed Successfully");
                } else {
                    JOptionPane.showMessageDialog(this, "Failed to Remove Supplier due to Some Reasons");
                }
            } catch (Exception ex) {
                Logger.getLogger(ManageSupplier.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnRemoveActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        
        String id = lblSupplierID.getText();
        
        String name = txtSupplierName.getText();
        
        if (!Validator.isValidName(name)) {
            JOptionPane.showMessageDialog(this, "Please enter a vaild name");
            txtSupplierName.selectAll();
            txtSupplierName.requestFocusInWindow();
            return;
        }
        
        String address = txtAddress.getText();
        
        if (!Validator.isValidAddress(address)) {
            JOptionPane.showMessageDialog(this, "Please enter a vaild address");
            txtAddress.selectAll();
            txtAddress.requestFocusInWindow();
            return;
        }
        
        String telNo = txtTelephoneNo.getText();
        
        if (!Validator.isValidTelNo(telNo)) {
            JOptionPane.showMessageDialog(this, "Please enter a vaild Telephone Number");
            txtTelephoneNo.selectAll();
            txtTelephoneNo.requestFocusInWindow();
            return;
        }
        
        int rsp = JOptionPane.showConfirmDialog(this, "Do you want to update this supplier ? ");
        if (rsp == 0) {
            
            try {
                boolean isUpdated = ManageSupplierController.updateSupplier(new SupplierDTO(id, name, address, telNo));
                if (isUpdated) {
                    clearAllTextFields();
                    getAllSupplierDetails();
                    JOptionPane.showMessageDialog(this, "Supplier Updated Successfully");
                } else {
                    JOptionPane.showMessageDialog(this, "Failed to Update Supplier due to some reasons");
                }
            } catch (Exception ex) {
                Logger.getLogger(ManageSupplier.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnUpdateActionPerformed


