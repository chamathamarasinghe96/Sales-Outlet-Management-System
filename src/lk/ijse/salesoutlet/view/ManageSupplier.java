    private void getAllSupplierDetails() {
        try {
            ArrayList<SupplierDTO> allSuppliers = ManageSupplierController.getAllSuppliers();
            dtm = (DefaultTableModel) tblSupplier.getModel();
            dtm.setRowCount(0);
            for (SupplierDTO supplier : allSuppliers) {
                dtm.addRow(new Object[]{
                    supplier.getId(),
                    supplier.getName(),
                    supplier.getAddress(),
                    supplier.getTelNo()
                });
            }
            dtm.addRow(new Object[]{});
        } catch (Exception ex) {
            Logger.getLogger(ManageSupplier.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
