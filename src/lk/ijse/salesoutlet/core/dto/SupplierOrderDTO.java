package lk.ijse.salesoutlet.core.dto;

/**
 *
 * @author user
 */
public class SupplierOrderDTO extends SuperDTO{
    private String orderId;
    private String paymentId;
    private String supplierId;
    private int productCount;
    private String date;

    public SupplierOrderDTO() {
    }

    public SupplierOrderDTO(String orderId, String paymentId, String supplierId, int productCount, String date) {
        this.orderId = orderId;
        this.paymentId = paymentId;
        this.supplierId = supplierId;
        this.productCount = productCount;
        this.date = date;
    }

    /**
     * @return the orderId
     */
    public String getOrderId() {
        return orderId;
    }

    /**
     * @param orderId the orderId to set
     */
    public void setOrderId(String orderId) {
        this.orderId = orderId;
    }

    /**
     * @return the paymentId
     */
    public String getPaymentId() {
        return paymentId;
    }

    /**
     * @param paymentId the paymentId to set
     */
    public void setPaymentId(String paymentId) {
        this.paymentId = paymentId;
    }

    /**
     * @return the supplierId
     */
    public String getSupplierId() {
        return supplierId;
    }

    /**
     * @param supplierId the supplierId to set
     */
    public void setSupplierId(String supplierId) {
        this.supplierId = supplierId;
    }

    /**
     * @return the productCount
     */
    public int getProductCount() {
        return productCount;
    }

    /**
     * @param productCount the productCount to set
     */
    public void setProductCount(int productCount) {
        this.productCount = productCount;
    }

    /**
     * @return the date
     */
    public String getDate() {
        return date;
    }

    /**
     * @param date the date to set
     */
    public void setDate(String date) {
        this.date = date;
    }
    
}
